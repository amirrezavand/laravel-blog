    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $factors=Factor::query()->with('user')->orderBy('id','desc');

        $keywords=$request->input('keywords');

        if($keywords) {
            $factors=$factors->whereHas('user', function ($query) use ($keywords){
                $query->where('name', 'like', '%'.$keywords.'%');
            })->orWhere('title','LIKE','%'.$keywords.'%')->paginate(10)->withQueryString();
        }
        else
            $factors=$factors->paginate(10)->withQueryString();

        return view('admin.factor.index',compact('factors'));
    }

